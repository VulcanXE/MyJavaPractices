public class Switchstatement {
    public static void main(String[] args) {
        int iphone = 12;
        switch (iphone) {
            case 4:
                System.out.println("iphone4");
                break;
            case 5:
                System.out.println("iphone5");
                break;
            case 6:
                System.out.println("iphone6");
                break;
            case 7:
                System.out.println("iphone7");
                break;
            case 10:
                System.out.println("iphone10");
                break;
            case 11:
                System.out.println("iphone11");
                break;
            case 12:
                System.out.println("iphone12");
                break;
            default:
                System.out.println("model not released.");
        }

    }
}
